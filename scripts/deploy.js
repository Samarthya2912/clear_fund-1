async function main() {
  const [deployer] = await ethers.getSigners();

  console.log("Deploying contracts with the account:", deployer.address);

  console.log("Account balance:", (await deployer.getBalance()).toString());

  const clearFund = await ethers.getContractFactory("clearFund");
  const ClearFund = await clearFund.deploy();

  console.log("clearFund address:", ClearFund.address);
}

main()
  .then(() => process.exit(0))
  .catch((error) => {
    console.error(error);
    process.exit(1);
  });